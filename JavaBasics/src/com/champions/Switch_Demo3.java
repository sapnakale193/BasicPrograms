package com.champions;

public class Switch_Demo3 {
	
	public static void main(String[]args) {
		
		String city="goa";
		switch(city)
		{
		case "mumbai":System.out.println("Welcome to Mumbai");
		break;
		case"Pune":System.out.println("Welcome to Pune");
		break;
		case"Goa":System.out.println("Welcome to Goa");
		break;
		default:System.out.println("Welcome to All india");
		}
	    }
        }
