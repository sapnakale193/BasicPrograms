package com.champions;

public class SwapNumbers {
	
	 public static void main(String[] args) {
	       
	    int x = 10, y = 20, temp;
	       
	    System.out.println("Before swapping: x = " + x + ", y = " + y);
	        
	    temp = x;
	       x = y;
	       y = temp;
	        
	    System.out.println("After swapping: x = " + x + ", y = " + y);
	    
	 
	}
	}


