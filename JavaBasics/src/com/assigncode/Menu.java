package com.assigncode;

public class Menu {
	
	private int m_id;
	private String menu_name;
	private int price;

	public void setM_id(int m_id) {
		this.m_id = m_id;
	}

	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getM_id() {
		return m_id;
	}

	public String getMenu_name() {
		return menu_name;
	}

	public int getPrice() {
		return price;
	}

}


  
	
	

