package com.demo;

public class Series {
	
	public static void main(String[] args) {
		
		int n =6; // Number of the elements in the series 
		
		int Num= 1;  // First Num of the series 
		
		System.out.println(Num + " ");
		
		for (int i =2; i <= n; i++) {
			
		Num = i * i * i + 1; // To calculate the next Num
		
		System.out.println(Num + " ");
		
		}
        }
        }
